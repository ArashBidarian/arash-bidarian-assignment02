package com.nackademin.arash_bidarian_assignment02;

public class BasicCalculator implements BasicOperations{

	public double addition(double firstNumber, double secondNumber) {
		return firstNumber + secondNumber;
	}

	public double substraction(double firstNumber, double secondNumber) {
		return firstNumber - secondNumber;
	}

	public double multipication(double firstNumber, double secondNumber) {
		return firstNumber * secondNumber;	}

	public double dicvision(double firstNumber, double secondNumber) {
		return firstNumber / secondNumber;		
		
	}

}
